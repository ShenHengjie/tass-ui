<template>
  <div class="tas-dropdown" @mouseenter="mouseenter" @mouseleave="mouseleave" @click="mouseclick">
    <slot></slot>
  </div>
</template>

<script setup lang="ts">
  import '../style/';
  import mitt from './event';

  const props = defineProps({
    trigger: {
      type: [String],
      default: 'hover'
    }
  });
  const mouseclick = () => {
    if (props.trigger === 'click') {
      mitt.emit('updateShowMenuClick', true);
    }
  };
  const mouseenter = () => {
    if (props.trigger === 'hover') {
      mitt.emit('updateShowMenu', true);
    }
  };
  const mouseleave = () => {
    mitt.emit('mouseleave', false);
  };
</script>
